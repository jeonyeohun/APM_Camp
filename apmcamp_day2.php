제 9조 (서비스의 중지 및 정보의 저장과 사용)

1. 회원은 회사 서비스에 보관되거나 전송된 메시지 및 기타 통신 메시지 등의 내용이 국가의  비상사태, 정전, 회사의 관리범위 외의 서비스 설비 장애 및 기타 불가항력에 의하여 보관되지  못하였거나 삭제된 경우, 전송되지 못한 경우 및 기타 통신 데이터의 손실이 있을 경우에  회사는 관련 책임을 부담하지 아니합니다.

