[제 1장] 총칙

제 1조 (목적)

본 약관은 (주)베스트웨딩넷(이하 "회사"라 칭함)이 제공하는 모든 서비스
(이하 "서비스"라 칭함)의 이용조건, 절차, 이용자(이하 "회원"이라 칭함)와 회사의 권리, 의무
책임사항과 기타 필요한 사항을 규정함을 목적으로 합니다.

제 2조 (약관의 효력과 변경)

1. 회사는 회원이 본 약관에 동의하는 조건으로 회원에게 서비스를 제공할 것이며,
   회원이 본 약관의 내용에 동의하는 경우, 회사의 서비스 제공 행위 및 회원의 서비스사용
   행위에는 본 약관이 우선적으로 적용됩니다.

2. 회사는 본 약관을 사전 고지 없이 변경할 수 있으며, 변경된 약관은 회사 사이트 내에 게시
   하여회원이 직접 확인 할 수 있도록 하고, 회원이 변경된 약관에 동의하지 아니하는 경우,  회원은 본인의 회원등록을 취소(회원탈퇴)할 수 있습니다.
   계속 사용의 경우는 약관 변경에 대한 동의로 간주됩니다.  변경된 약관은 회사 사이트에 게시함과 동시에 그 효력이 발생됩니다.

제 3조 (약관의 명시 외 준칙)

1. 본 약관은 회사가 제공하는 서비스에 관한 이용규정 및 별도 약관과 함께 적용됩니다.

2. 본 약관에 명시되지 않은 사항은 전기통신기본법, 전기통신사업법, 정보통신 윤리위원회
   심의규정, 정보통신 윤리강령, 프로그램 보호법 및 방문판매등에 관한 법률, 소비자보호법 등
   관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다.

3. 이 약관에서 정하지 아니한 사항과 이 약관의 해석에 관하여는 정부가 제정한 전자거래소비자
   보호지침 및 관계법령 또는 상관례에 따릅니다

제 4조 (용어의 정의)

본 약관에서 사용하는 용어의 정의는 다음과 같습니다.

1. 회원 : 회사에 개인 정보를 제공하여 회원 등록을 한 자로서, 회사의 정보를 제공받으며,  회사가 제공하는 서비스를 이용할 수 있는 자.

2. 회원ID : 회원식별과 회원의 서비스 이용을 위하여 회원이 신청하고 회사가 승인하는  문자 및 숫자의 조합

3. 비밀번호 : 이용자와 회원ID가 일치하는지를 확인하고 통신상의 자신의 비밀보호를 위하여  이용자 자신이 선정한 문자와 숫자의 조합.

4. 탈퇴 : 회원이 이용계약을 종료 시키는 행위

5. 협력사:회사와 업무제휴관계로 유,무형의 컨텐츠를 제공한 업체

6. 본 약관에서 정의하지 않은 용어는 개별서비스에 대한 별도 약관 및 이용규정에서 정의 합니다.

[제 2장] 서비스 제공 및 이용

제 5조 (이용 계약의 성립)

1. 이용계약은 신청자가 온라인으로 회사 소정의 가입신청 양식에서 요구하는 사항을 기록하여 가입을 완료 하는 것으로 성립됩니다.

2. 회사는 다음 각 호에 해당하는 이용계약에 대하여는 가입을 취소할 수 있습니다.

  1) 타인의 주민번호 또는 정보로 가입한 경우
  2) 이용 계약 신청서의 내용을 허위로 기재하였거나 허위서류를 첨부하여 신청하였을 때
  3) 사회의 안녕 질서 혹은 미풍양속을 저해할 목적으로 신청하거나 그러한 행위를 하였을 때
  4) 다른 사람의 회사서비스 이용을 방해하거나 그 정보를 도용하는 등의 행위를 하였을 때
  5) 전기통신기본법, 전기통신사업법, 정보통신 윤리위원회 심의규정, 정보통신 윤리강령, 프로그램 보호법 및 기타관련 법령과 본 약관이 금지하는 행위를 하는 경우
  6) 기타 회사가 정한 이용신청요건이 미비 되었을 때
  7) 현찰포인트 제도를 악용한 경우

3. 회사는 다음 각 항에 해당하는 경우 그 사유가 해소될 때까지 이용계약 성립을  유보할 수 있습니다.

  1) 서비스 관련 제반 용량이 부족한 경우
  2) 기술상 장애 사유가 있는 경우

제 6조 (회원정보 사용에 대한 동의)

1. 회원의 개인정보에 대해서는 회사의 개인정보 보호정책이 적용됩니다.

2. 회사의 회원 정보는 다음과 같이 수집, 사용, 관리, 보호됩니다.
  1) 개인정보의 수집 : 회사는 회원의 회사 서비스 가입 시 회원이 제공하는 정보, 커뮤니티  활동을 위하여 회원이 제공하는 정보, 각종 이벤트 참가를 위하여 회원이 제공하는 정보,  광고나 경품의 취득을 위하여 회원이 제공하는 정보 등을 통하여 귀하에 관한 정보를  수집합니다.

  2) 개인정보의 사용 : 회사는 회사 서비스 제공과 관련해서 수집된 회원의 신상정보를 본인의 승낙 없이 제3자에게 누설, 배포하지 않습니다. 단, 아래 경우에는 그러하지 않습니다.
   ① 전기통신기본법 등 법률의 규정에 의해 국가기관의 요구가 있는 경우
   ② 범죄에 대한 수사상의 목적이 있거나 정보통신윤리 위원회의 요청이 있는 경우   또는 기타 관계법령에서 정한 절차에 따른 요청이 있는 경우
   ③ 회사에 가입한 미성년자의 법정대리인이 정보를 요청하여 서류상으로 가족관계가 확인된  경우
   ④ 회원이 회사에 제공한 개인정보를 스스로 공개한 경우
  3) 개인정보의 관리 : 회원은 개인정보의 보호 및 관리를 위하여 서비스의 개인정보관리에서 수시로 회원의 개인정보를 수정/삭제할 수 있습니다. 수신되는 정보 중 불필요하다고 생각되는 부분도 변경/조정할 수 있습니다.
  4) 개인정보의 보호 : 회원의 개인정보는 오직 귀하만이 열람/수정/삭제 할 수 있으며, 이는  전적으로 회원의 ID와 비밀번호에 의해 관리되고 있습니다. 따라서 타인에게 본인의 ID와  비밀번호를 알려주어서는 아니 되며, 작업 종료 시에는 반드시 로그아웃해 주시고, 웹 브라우저의 창을 닫아 주시기 바랍니다.
  5) 기타 : 게시판이나, E-mail, 채팅 등 온라인상에서 회원이 자발적으로 제공하는 개인정보는   다른 사람들이 수집하여 사용할 수 있음을 인지하시기 바랍니다. 공개적인 공간에 게재되는 개인정보로 인해 원하지 않는 상황이 발행할 수도 있습니다. 개인정보에 대한 비밀을 유지할 책임은 회원에게 있으며, 회사는 개인정보 유출로 인해 발생한 결과에 대하여 어떤 책임도 부담하지 아니합니다.

3. 회사는 회원에게 맞춤 서비스, 인터넷 쇼핑 서비스, 커뮤니케이션 서비스, 멤버쉽 카드,기타  다양한 서비스를 제공할 목적으로 여러 분야의 전문 컨텐츠 사업자 혹은 비즈니스 사업자와 함께 제휴 혹은 파트너쉽을 맺어 이용자에게 제공할 수 있습니다.

4. 회사는 회사가 제공하는 서비스를 이용하는 회원을 대상으로 해당서비스의 양적, 질적향상 및  멤버쉽카드 발급을 위하여 회원이 가입 시 기재한 회원정보를 수집하여 전화를 통한  신규 및 제휴서비스 안내 등에 이용할 수 있습니다.

5. 회사는 업무와 관련하여 회원 전체, 일부의 개인 정보에 관한 자료를 작성하여 이를  사용할 수 있고, 서비스를 통하여 회원의 컴퓨터에 쿠키를 전송할 수 있습니다 동의를얻어야  합니다.

6. 당사는 회원가입 시 주민번호를 사용하지 않습니다

